/**
 * [一维数组]
 * 元素类型、数组名和长度组成的构造类型。
 * 数组长度在定义时已经确定，用常量表达式表示
 * 
 * [一维数组内存形式]
 * 数组元素是连续存放的，
 * 内存中一个“很大的变量”，简称“块”
 * 索引元素是通过数组名加相对偏移量而来 A[n],数组A中偏移量n-1的元素
 *
 * [数组对象初始化]
 * 元素类型 数组名[常量表达式]={初值列表},......;
 * 
 * int A[5]={1,2,3},总共5个，只给出3个，后两位默认为0
 * 
 * [计算数组int A[n]的长度]
 * sizeof A/sizeof(int)//数组内存长度/元素内存长度=数组长度
 * 
 * 数组未初始化，函数体外静态数组元素默认0，函数体内动态数组元素为随机值
 * 
 * "[]":下标运算符
 * 
 * 只能对数组元素赋值，不能对整个数组赋值
*/
#include <stdio.h>
int main()
{
    int i,j;
    int a[10]={4,2,7,1,9,10,3,5,2,8};
    for(i=1;i<10;i++){
        for(j=1;j<=a[i-1];j++)
            printf("*");
        printf("\n");
    }
    return 0;
}
