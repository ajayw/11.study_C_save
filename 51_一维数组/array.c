/**
 * [数组与枚举]
 * 数码管高亮   hh:mm 时：分
 * 0<=hh<=24,0<=mm<=60;
 * 
 * 
*/
#include <stdio.h>
int s[] = {6,2,5,5,4,5,6,3,7,6}; 
int main ()
{
    int hh,mm,n,flag;
    while (scanf("%d", &n)!=EOF) {//直到没有读取到有输入值
        flag=0;
        for (hh = 0;hh<24 && flag==0;hh++)
            for (mm = 0;mm<60 && flag==0;mm++)
                if (s[hh%10]+s[hh/10]+s[mm%10]+s[mm/10]==n) {
                    printf ("%02d:%02d\n", hh, mm); 
                    flag=1;
                }
                if (flag==0)
                    printf ("Impossible\n"); 
    }
}

